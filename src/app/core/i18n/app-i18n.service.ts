@Injectable({
  providedIn: 'root',
})
export class AppI18nService {
  private currentLanguageValue: AppLanguage = this.readStoredLanguage();
  private readonly warnedMissingKeys = new Set<string>();

  get currentLanguage(): AppLanguage {
    return this.currentLanguageValue;
  }

  setLanguage(language: string | null | undefined): void {
    const nextLanguage: AppLanguage =
      language === 'en' || language === 'ja' || language === 'vi' ? language : 'vi';
    this.currentLanguageValue = nextLanguage;
    localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
  }

  translate(key: string): string {
    const translated =
      TRANSLATIONS[this.currentLanguageValue]?.[key] ||
      TRANSLATIONS.vi[key];

    if (translated) {
      return translated;
    }

    this.warnMissingKey(key);
    return this.humanizeKey(key);
  }

  private readStoredLanguage(): AppLanguage {
    const raw = localStorage.getItem(LANGUAGE_STORAGE_KEY);
    return raw === 'en' || raw === 'ja' || raw === 'vi' ? raw : 'vi';
  }

  private warnMissingKey(key: string): void {
    const warningKey = `${this.currentLanguageValue}:${key}`;
    if (this.warnedMissingKeys.has(warningKey)) {
      return;
    }

    this.warnedMissingKeys.add(warningKey);
    console.warn(`[i18n] Missing translation for "${key}" in "${this.currentLanguageValue}".`);
  }

  private humanizeKey(key: string): string {
    const leaf = key.split('.').pop() || key;
    const normalized = leaf
      .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
      .replace(/[_-]+/g, ' ')
      .trim();

    if (!normalized) {
      return key;
    }

    const aliases: Record<string, string> = {
      ai: 'AI',
      api: 'API',
      db: 'DB',
      id: 'ID',
      hsx: 'HSX',
      hnx: 'HNX',
      upcom: 'UPCOM',
    };

    return normalized
      .split(/\s+/)
      .map((part) => {
        const alias = aliases[part.toLowerCase()];
        if (alias) {
          return alias;
        }

        return part.charAt(0).toUpperCase() + part.slice(1);
      })
      .join(' ');
  }
}
